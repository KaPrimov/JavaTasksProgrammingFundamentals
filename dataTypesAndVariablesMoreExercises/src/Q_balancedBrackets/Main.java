package Q_balancedBrackets;

public class Main {
    public static void main(String[] args) {
        double num1 = (4.22 * 9450000000000d);
        double num2 = 26000 * 9450000000000d;
        double num3 = (100000 * 9450000000000d);
        double num4 = 46500000000d * 9450000000000d;

        System.out.printf("%.2e%n", num1);
        System.out.printf("%.2e%n", num2);
        System.out.printf("%.2e%n", num3);
        System.out.printf("%.2e%n", num4);
        System.out.println("3.99e+013\n" +
                "2.46e+017\n" +
                "9.45e+017\n" +
                "4.39e+023");
    }
}
